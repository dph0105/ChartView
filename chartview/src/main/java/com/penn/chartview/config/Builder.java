package com.penn.chartview.config;

import android.graphics.Color;

/**
 * @author: DaDing
 * @description:
 * @create: 2018-11-13 15:20
 **/
public abstract class Builder {

    /**
     * 设置x轴宽度
     */
    public abstract void setXAxisWidth(float width);

    /**
     * 设置x轴颜色
     */
    public abstract void setXAxisColor(int color);

    /**
     * 设置y轴宽度
     */
    public abstract void setYAxisWidth(float width);

    /**
     * 设置y轴颜色
     */
    public abstract void setYAxisColor(float color);


    /**
     * 设置线宽
     */
    public abstract void setLineWidth(float width);


    /**
     * 设置线的颜色
     * @param color
     */
    public abstract void setLineColor(float color);










}
